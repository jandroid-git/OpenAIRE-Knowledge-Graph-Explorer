

def add(a, b):
    """
    Adds two numbers together.

    :param a: First number
    :param b: Second number
    :return: Sum of a and b
    """
    return a + b


def subtract(a, b):
    """
    Subtracts the second number from the first one.

    :param a: First number
    :param b: Second number
    :return: Difference between a and b
    """
    return a - b
